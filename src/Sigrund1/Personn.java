package Sigrund1;

public class Personn {


    private  String name;
    private  String adress;
    private  int age;

      public Personn (String name, String adress, int age){
        this.name = name;
        this.adress = adress;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getAdress() {
        return adress;
    }

    public int getAge() {
        return age;
    }
}




